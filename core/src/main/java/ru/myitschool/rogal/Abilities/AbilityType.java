package ru.myitschool.rogal.Abilities;

/**
 * Перечисление для категоризации различных типов способностей
 */
public enum AbilityType {
    /** Наносит урон противникам */
    ATTACK,
    
    /** Восстанавливает здоровье или дает защитные эффекты */
    DEFENSIVE,
    
    /** Улучшает характеристики игрока */
    BUFF,
    
    /** Накладывает отрицательные эффекты на противников */
    DEBUFF,
    
    /** Дает пассивные улучшения */
    PASSIVE,
    
    /** Особые способности, не подходящие под другие категории */
    SPECIAL
} 